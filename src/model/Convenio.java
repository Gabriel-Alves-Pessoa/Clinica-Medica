package model;

/**
 *
 * @author Rivaldo
 */
public class Convenio {
    private int id_con;
    private String cnpj_con;
    private String nome_con;

    public int getId_con() {
        return id_con;
    }

    public void setId_con(int id_con) {
        this.id_con = id_con;
    }

    public String getCnpj_con() {
        return cnpj_con;
    }

    public void setCnpj_con(String cnpj_con) {
        this.cnpj_con = cnpj_con;
    }

    public String getNome_con() {
        return nome_con;
    }

    public void setNome_con(String nome_con) {
        this.nome_con = nome_con;
    }  
    
}
